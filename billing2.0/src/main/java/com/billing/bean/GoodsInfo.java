package com.billing.bean;

public class GoodsInfo {
	private String charges;
	private String appname;
	private String appprovide;
	private String servicephonenum;

	public GoodsInfo() {
		super();
	}

	public String getCharges() {
		return charges;
	}

	public void setCharges(String charges) {
		this.charges = charges;
	}

	public String getAppname() {
		return appname;
	}

	public void setAppname(String appname) {
		this.appname = appname;
	}

	public String getAppprovide() {
		return appprovide;
	}

	public void setAppprovide(String appprovide) {
		this.appprovide = appprovide;
	}

	public String getServicephonenum() {
		return servicephonenum;
	}

	public void setServicephonenum(String servicephonenum) {
		this.servicephonenum = servicephonenum;
	}

}
